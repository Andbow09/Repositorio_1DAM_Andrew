package org.example.Practica2;

/**
 * Interfaz que define las funciones generales de los integrantes del equipo.
 */
public interface FuncionesIntegrantes {
    /**
     * Simula la concentración antes de un partido.
     */
    void concentrarse();

    /**
     * Simula un viaje a una ciudad.
     * @param ciudad Ciudad a la que se viaja.
     */
    void viajar(String ciudad);

    /**
     * Simula la celebración de un gol.
     */
    void celebrarGol();
}
